--
-- fasttrun_silent — поведение fasttruncate на пограничных таблицах:
--   * несуществующая таблица — функция молча ничего не делает (без ошибки)
--   * обычная (не временная) таблица — выдаёт ошибку, чтобы поймать опечатку
--

CREATE EXTENSION fasttrun;

-- ----------------------------------------------------------------------
-- 1. Несуществующая таблица — fasttruncate просто молчит
-- ----------------------------------------------------------------------
SELECT fasttruncate('temp_does_not_exist_xyz');

-- Проверяем, что несуществующее имя со схемой тоже отрабатывает молча
SELECT fasttruncate('pg_temp.temp_does_not_exist_xyz');

-- И что после "молчания" соединение по-прежнему живо и в нём можно
-- делать обычные SQL — то есть мы не оставили висящих блокировок
-- и не сломали транзакцию.
CREATE TEMP TABLE t_silent (a int);
INSERT INTO t_silent SELECT generate_series(1, 5);
SELECT count(*) FROM t_silent;

-- Несуществующее имя при наличии нормальной соседней временной таблицы
SELECT fasttruncate('also_does_not_exist');
SELECT count(*) FROM t_silent;

-- ----------------------------------------------------------------------
-- 2. Обычная (не временная) таблица — должна быть ошибка, а не молчание.
--    Это сознательная защита от опечаток в имени; вызывающий расчёт
--    точно не хотел очищать постоянную таблицу.
-- ----------------------------------------------------------------------
CREATE TABLE t_persistent (a int);
INSERT INTO t_persistent VALUES (1), (2), (3);

-- Должна быть ошибка с понятным сообщением:
SELECT fasttruncate('t_persistent');

-- Данные в постоянной таблице должны остаться нетронутыми
SELECT count(*) FROM t_persistent;

-- ----------------------------------------------------------------------
-- 3. После ошибки соединение должно быть пригодно к работе
-- ----------------------------------------------------------------------
SELECT 1 AS still_alive;

-- Очистка
DROP TABLE t_silent;
DROP TABLE t_persistent;
DROP EXTENSION fasttrun;
